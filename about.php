es la Unidad Educativa se convirtió en  un  espacio interacción social, por donde estos últimos 32 años pasaron por sus aulas niños y jóvenes  habidos de conocimiento los cuales se encuentran hoy en día cumpliendo tareas muy importantes dentro la sociedad boliviana como profesionales destacados contribuyendo al desarrollo del país, por sus aulas también pasaron    docentes con vocación de servicio quienes aportaron al engrandecimiento de las personas y de su entorno.</p>
